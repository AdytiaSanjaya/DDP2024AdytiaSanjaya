import math

def tambah(bil1, bil2):
    hasil = 3 + 9
    print("Hasil Tambah Dari",3, "+", 9, "=", hasil)

def kurang(bil1, bil2):
    hasil = 10 - 6
    print("Hasil Pengurangan Dari",10, "-", 6, "=", hasil)

def kali(bil1, bil2):
    hasil = 3 * 3
    print("Hasil Perkalian Dari",3, "*", 3, "=", hasil)

def bagi(bil1, bil2):
    hasil = 20 / 4
    print("Hasil Pembagian Dari",20, "/", 4, "=", hasil)

def pangkat(bil1, bil2):
    hasil = math.pow(3, 3)
    print("Hasil Pemangkatan Dari",3, "^",3,"=",hasil)